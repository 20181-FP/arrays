public class Vec01{
	public static void main(String[] args) {
		int ent[] = {5,6,7,8,12,-9};
		System.out.println("ent[0]="+ent[0]);

		for (int i=0;i<ent.length ;i++ ) {
			System.out.println("ent["+i+"]="+ent[i]);
		}

		int[] entero = new int[5];
		entero[0]=5;
		entero[1]=6;
		entero[2]=7;
		entero[3]=8;
		entero[4]=12;
		System.out.println("entero[0]="+entero[0]);
	}
}